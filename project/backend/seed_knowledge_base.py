def seed_all():
    print("=" * 60)
    print("开始向 ChromaDB 灌入种子医疗知识...")
    print("=" * 60)

    all_docs = {
        "medical_kb": MEDICAL_KB_DOCS,
        "drug_kb": DRUG_KB_DOCS,
        "rehab_kb": REHAB_KB_DOCS,
        "checkup_kb": CHECKUP_KB_DOCS,
        "geriatric_kb": GERIATRIC_KB_DOCS,
    }

    for coll_name, docs in all_docs.items():
        print(f"\n📦 {coll_name} ({len(docs)} 篇文档)")
        # 格式化文档
        formatted = [{"content": d["content"], "metadata": d["metadata"]} for d in docs]
        count = index_documents(documents=formatted, collection=coll_name)
        print(f"   ✅ 已写入 {count} 条")

    print("\n" + "=" * 60)
    print("种子知识库灌入完成！各 collection 文档数量：")
    stats = get_collection_stats()
    for name, cnt in stats.items():
        print(f"  {name}: {cnt} 条")
    print("=" * 60)


if __name__ == "__main__":
    seed_all()
